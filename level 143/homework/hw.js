// 1) Git — ეს არის პროგრამა, რომელიც "ამახსოვრებს" ჩვენი კოდის ცვლილებებს,
// რომ ნებისმიერ დროს შევძლოთ უკან დაბრუნება, ვნახოთ რა შეიცვალა და ვინ შეცვალა.
// GitHub — ეს არის ვებგვერდი/პლატფორმა, სადაც შეგვიძლია ჩვენი Git პროექტები
// ავტვირთოთ და სხვებთან გავაზიაროთ. ანუ Git არის თვითონ ინსტრუმენტი,
// GitHub კი — ადგილი სადაც კოდს ვინახავთ და ვაკონტროლებთ ონლაინში.

// 2) Commit — ეს არის "შენახვის წერტილი" Git-ში.
// როცა commit-ს ვაკეთებთ, ვეუბნებით Git-ს: "აი აქამდე მოდიფიცირებული კოდი
// დამიმახსოვრე". ასე შეგვიძლია ნებისმიერ დროს დავბრუნდეთ კონკრეტულ commit-ზე,
// ან ვნახოთ რა შეიცვალა.

// 3) Main branch — ეს არის მთავარი ხაზი (ფილიალი) პროექტის,
// სადაც ყველაზე სტაბილური და მთავარი კოდი ინახება.
// სხვა branch-ები კი იმისთვის გვჭირდება, რომ ცალკე დავწეროთ ახალი ფუნქცია,
// შევცვალოთ რაღაცა ან ვცადოთ ექსპერიმენტი ისე, რომ მთავარი კოდი არ დავაზიანოთ.
// მერე როცა ყველაფერი მზად იქნება, ამ branch-ს ვაერთიანებთ main-ში.